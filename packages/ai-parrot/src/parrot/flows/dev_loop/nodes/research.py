"""ResearchNode — bug triage, Jira ticket, sdd-research dispatch.

Implements **Module 5** of the dev-loop spec. Sequence (per spec §3 M5):

1. Fetch logs via the configured log toolkits (one per ``LogSource.kind``).
2. Create the Jira ticket via ``jira_toolkit.jira_create_issue(...)``.
   The ticket is created **before** the dispatch — the call order is
   pinned by a unit test (spec §4 ``test_research_node_creates_jira_then_dispatches``).
   FEAT-132: the issuetype is derived from ``brief.kind`` via
   ``_ISSUE_TYPE_BY_KIND``. On the create path, the plan summary is
   generated by ``_build_plan_summary`` and posted as the first comment.
3. Dispatch to the ``sdd-research`` subagent so it runs ``/sdd-spec``,
   ``/sdd-task``, and creates the worktree.
4. Validate the dispatch payload as :class:`ResearchOutput` (delegated
   to the dispatcher's output_model contract).

Spec §7 R5 — duplicate worktree detection: the node checks
``WORKTREE_BASE_PATH/<branch_name>`` after the subagent returns and
fails fast if the directory exists; recovery is the human's job.
"""

from __future__ import annotations

import asyncio
import os
import re
from typing import Any, Dict, List, Optional, Union

from navconfig import BASE_DIR

from parrot import conf
from parrot.bots.flows.core.context import FlowContext
from parrot.bots.flows.core.types import DependencyResults
from parrot.clients.factory import LLMFactory
from parrot.flows.dev_loop.dispatcher import ClaudeCodeDispatcher
from parrot.flows.dev_loop.models import (
    BugBrief,
    ClaudeCodeDispatchProfile,
    LogSource,
    RepoSpec,
    ResearchOutput,
    WorkBrief,
)
from parrot.flows.dev_loop.nodes.base import DevLoopNode, register_dev_loop_node


# Atlassian caps the description field at 32 767 chars; leave a 2K
# headroom for the rest of the JSON body and any unicode-byte expansion.
_MAX_DESCRIPTION_CHARS = 30_000
# Target size for the LLM-summarized excerpts block when the raw body
# would exceed _MAX_DESCRIPTION_CHARS. Keeps the digest compact enough
# that the surrounding sections (criteria, reporter, user description)
# always fit.
_SUMMARIZED_EXCERPTS_CHARS = 8_000

# Max number of cleaned log lines kept per source after noise filtering.
# CloudWatch Insights returns up to 100 rows; the vast majority are health
# probes and framework chatter, so a small tail of *meaningful* lines is
# all a human triager (or the research subagent) actually needs.
_MAX_CLEAN_LOG_LINES = 25

# Health-check / probe / framework-chatter lines that carry no incident
# signal. Dropped before excerpts ever reach the Jira ticket or the
# research prompt. Matched case-insensitively against the @message text.
_LOG_NOISE_RE = re.compile(
    r"""
      kube-probe                       # k8s liveness/readiness probes
    | /healthz?\b                      # GET /health or /healthz endpoints
    | "GET\s+/health                   # access-log health hits
    | httplog\.go.*?/healthz           # apiserver healthz access logs
    | Rendered\s                       # Rails view rendering
    | Processing\s+by\s.*Controller\#  # Rails controller dispatch
    | Started\s+GET\s+"/"              # Rails root probe
    """,
    re.IGNORECASE | re.VERBOSE,
)

# CloudWatch system fields that are pure cursor/pagination noise — never
# useful to a human and they dominate the byte count of a raw dump.
_LOG_DROP_FIELDS = frozenset({"@ptr"})

# FEAT-132: Jira issuetype per work-kind.
# Keys are the three WorkKind literals; values are the Jira issuetype
# labels accepted by the NAV Jira instance (verified 2026-04-28).
_ISSUE_TYPE_BY_KIND: Dict[str, str] = {
    "bug": "Bug",
    "enhancement": "Story",
    "new_feature": "New Feature",
}


def _summarizer_llm_default() -> str:
    """Resolve the default LLM string for log-excerpt summarization.

    Reads ``DEV_LOOP_SUMMARY_LLM`` from navconfig (env-overridable);
    falls back to Anthropic Haiku 4.5 — small, fast, cheap, perfect
    for compressing a few KB of log into a digest.
    """
    return conf.config.get(
        "DEV_LOOP_SUMMARY_LLM",
        fallback="anthropic:claude-haiku-4-5-20251001",
    )


def _plan_llm_default() -> str:
    """Resolve the plan-summary LLM string (FEAT-132).

    Returns the value of ``DEV_LOOP_PLAN_LLM`` when set, otherwise
    falls back to the same model as :func:`_summarizer_llm_default`.
    An empty ``DEV_LOOP_PLAN_LLM`` means "use the summarizer default".
    """
    pinned = conf.DEV_LOOP_PLAN_LLM
    if pinned:
        return pinned
    return _summarizer_llm_default()


@register_dev_loop_node("dev_loop.research")
class ResearchNode(DevLoopNode):
    """Second node — Jira + log fetch + sdd-research dispatch.

    Args:
        dispatcher: A :class:`ClaudeCodeDispatcher` instance shared by
            every node in the flow.
        jira_toolkit: A pre-built ``parrot_tools.jiratoolkit.JiraToolkit``
            wired with service-account credentials.
        log_toolkits: Mapping ``"cloudwatch"|"elasticsearch"`` →
            toolkit instance. Optional kinds may be missing; an unknown
            ``LogSource.kind`` raises ``ValueError`` at dispatch time.
        name: Node id, default ``"research"``.
    """

    def __init__(
        self,
        *,
        dispatcher: ClaudeCodeDispatcher,
        jira_toolkit: Any,
        log_toolkits: Optional[Dict[str, Any]] = None,
        summarizer_llm: Optional[str] = None,
        plan_llm: Optional[str] = None,
        git_toolkit: Optional[Any] = None,
        repos: Optional[List[RepoSpec]] = None,
        name: str = "research",
    ) -> None:
        super().__init__(node_id=name)
        object.__setattr__(self, "_dispatcher", dispatcher)
        object.__setattr__(self, "_jira", jira_toolkit)
        # FEAT-250: optional repo provisioning before Development. When
        # ``repos`` is empty the node behaves exactly as before (no clone).
        object.__setattr__(self, "_git_toolkit", git_toolkit)
        object.__setattr__(self, "_repos", list(repos or []))
        object.__setattr__(self, "_log_toolkits", log_toolkits or {})
        object.__setattr__(self, "_summarizer_llm", summarizer_llm or _summarizer_llm_default())
        object.__setattr__(self, "_summarizer_client", None)
        object.__setattr__(self, "_plan_llm", plan_llm or _plan_llm_default())
        object.__setattr__(self, "_plan_client", None)

    # ------------------------------------------------------------------
    # Execute
    # ------------------------------------------------------------------

    async def execute(
        self,
        ctx: Union[FlowContext, Dict[str, Any]],
        deps: Optional[DependencyResults] = None,
        **kwargs: Any,
    ) -> ResearchOutput:
        """Run the research phase. Returns a validated :class:`ResearchOutput`.

        Reads ``bug_brief`` and ``run_id`` from the shared state; writes
        ``jira_issue_key``, ``log_excerpts``, and ``research_output``.
        """
        shared = self.shared_state(ctx)
        brief: BugBrief = shared["bug_brief"]

        # 1. Fetch logs first — cheap, deterministic, and the excerpts
        # become part of the Jira description.
        excerpts = await self._collect_log_excerpts(brief.log_sources)

        # 2. Resolve the Jira ticket BEFORE dispatching. Unit tests pin
        # this ordering (spec §4 test_research_node_creates_jira_then_dispatches).
        # Idempotency: reuse an existing ticket when one already tracks
        # this incident (caller-supplied ``existing_issue_key`` or an
        # open ticket whose summary matches verbatim). Otherwise, fall
        # back to creating a new one. Reporter is resolved to an
        # accountId here (the toolkit auto-resolves the assignee but
        # not the raw fields={"reporter":…} blob, so we do it
        # explicitly so emails work in BugBrief.reporter).
        description = await self._build_description(brief, excerpts)
        existing_key = await self._find_existing_issue(brief)
        if existing_key:
            issue_key = existing_key
            self.logger.info(
                "Re-using existing Jira ticket %s for run_id=%s",
                issue_key, shared.get("run_id", "?"),
            )
            await self._comment_retriggered(
                issue_key=issue_key,
                run_id=shared.get("run_id", ""),
                description=description,
            )
        else:
            reporter_fields = await self._reporter_fields(brief.reporter)
            # FEAT-132: pick the issuetype from the kind→type mapping.
            issuetype = _ISSUE_TYPE_BY_KIND.get(
                getattr(brief, "kind", "bug"), "Bug"
            )
            jira_resp = await self._jira.jira_create_issue(
                summary=brief.summary,
                issuetype=issuetype,
                description=description,
                assignee=conf.FLOW_BOT_JIRA_ACCOUNT_ID or None,
                fields=reporter_fields,
            )
            issue_key = self._extract_issue_key(jira_resp)
            self.logger.info(
                "Created new Jira ticket %s (kind=%s) for run_id=%s",
                issue_key, getattr(brief, "kind", "bug"), shared.get("run_id", "?"),
            )
            # FEAT-132 G4: post plan-summary as first comment on new tickets.
            run_id = shared.get("run_id", "")
            plan = await self._build_plan_summary(brief, excerpts)
            await self._post_plan_summary_comment(
                issue_key=issue_key, plan=plan, run_id=run_id
            )
        shared["jira_issue_key"] = issue_key

        # 3. FEAT-253: Provision repos BEFORE the sdd-research dispatch so
        # the dispatch cwd is set correctly (clone path when a repo is
        # declared, WORKTREE_BASE_PATH when local).
        # _provision_repos now returns str(BASE_DIR) when no repos are
        # configured (local fallback) and the clone path otherwise.
        repo_path = await self._provision_repos(shared.get("run_id", ""))
        is_clone = bool(self._repos and self._git_toolkit is not None)
        dispatch_cwd: str = (
            repo_path if is_clone else os.path.abspath(conf.WORKTREE_BASE_PATH)
        )
        self.logger.info(
            "Base repository: %s (clone=%s), dispatch cwd: %s",
            repo_path, is_clone, dispatch_cwd,
        )

        # 4. Dispatch the sdd-research subagent.
        profile = ClaudeCodeDispatchProfile(
            subagent="sdd-research",
            permission_mode="acceptEdits",
            # SlashCommand: the subagent runs /sdd-spec and /sdd-task.
            # Write: it scaffolds spec/task files under sdd/. Read/Grep/Glob
            # for triage, Bash for git worktree plumbing.
            allowed_tools=[
                "Read", "Grep", "Glob", "Bash", "Write", "SlashCommand",
            ],
            model="claude-sonnet-4-6",
        )
        os.makedirs(dispatch_cwd, exist_ok=True)
        # Stash the excerpts on the brief so the subagent gets them.
        # We pass the brief through as-is since BugBrief already carries
        # log_sources; the prompt builder embeds excerpts separately.
        shared["log_excerpts"] = excerpts

        research_out: ResearchOutput = await self._dispatcher.dispatch(
            brief=brief,
            profile=profile,
            output_model=ResearchOutput,
            run_id=shared["run_id"],
            node_id=self.name,
            cwd=dispatch_cwd,
            # FEAT-322: fold dispatch-level events into the run's
            # SessionHost when one is present (see development.py's
            # dispatch() call for the same pattern/rationale).
            session_host=shared.get("session_host"),
        )

        # 5. If the subagent left jira_issue_key blank, inject ours.
        if not research_out.jira_issue_key:
            research_out = research_out.model_copy(
                update={"jira_issue_key": issue_key}
            )

        # 6. Spec §7 R5 (relaxed) — reuse existing worktree if it's
        # already a registered git worktree on the expected branch;
        # fail fast only on the unsafe shapes (untracked directory or
        # mismatched branch).
        await self._ensure_worktree_safe(research_out.branch_name)

        # 7. Set repo_path on the output to record the base repository.
        # repo_path is str(BASE_DIR) (local) or the clone path (declared
        # repo). worktree_path remains the per-run worktree — distinct from
        # repo_path (FEAT-253 G2/G3).
        research_out = research_out.model_copy(
            update={"repo_path": repo_path}
        )

        shared["research_output"] = research_out
        return research_out

    # ------------------------------------------------------------------
    # Internal — repo provisioning (FEAT-250)
    # ------------------------------------------------------------------

    async def _provision_repos(self, run_id: str) -> str:
        """Clone/pull each configured ``RepoSpec``; return the base repo path.

        **Local fallback (FEAT-253 G2)**: when no repos are declared or no
        ``git_toolkit`` is set, the base repository is the local checkout at
        ``BASE_DIR``. Returns ``str(BASE_DIR)`` (no clone; worktree will be
        branched from the outer ``BASE_DIR`` repo by the ``sdd-research``
        dispatch).

        **Declared-repo path (FEAT-253 G3)**: the primary repo (first
        ``RepoSpec``) is cloned/pulled into
        ``<DEV_LOOP_REPO_BASE_PATH>/<run_id>/<alias>`` — anchored under
        ``WORKTREE_BASE_PATH`` so the dispatcher's cwd-safety guard passes.
        Returns the clone path (the base repository from which the worktree
        will be created). Secondary repos are **not** cloned in v1.

        Never logs tokens — :class:`GitToolkit` handles scrubbing internally.
        """
        if not self._repos or self._git_toolkit is None:
            self.logger.info(
                "No repos declared; base repository = BASE_DIR (%s)", BASE_DIR
            )
            return str(BASE_DIR)

        base = os.path.join(
            os.path.abspath(conf.DEV_LOOP_REPO_BASE_PATH), run_id or "run"
        )
        os.makedirs(base, exist_ok=True)

        primary_path = ""
        for idx, repo in enumerate(self._repos):
            dest = os.path.join(base, repo.alias)

            # Defense-in-depth: alias already validated by RepoSpec, but guard
            # against any normpath escape that slips through at the OS level.
            if not os.path.normpath(dest).startswith(os.path.normpath(base)):
                raise ValueError(
                    f"RepoSpec alias {repo.alias!r} would escape the clone "
                    f"base {base!r} — refusing to provision."
                )

            self.logger.info(
                "Provisioning repo %r → %s (branch=%s, private=%s)",
                repo.alias, dest, repo.branch, repo.private,
            )
            try:
                result = await self._git_toolkit.clone_repo(
                    repo.url,
                    dest,
                    branch=repo.branch,
                    private=repo.private,
                )
            except Exception as exc:
                self.logger.error(
                    "Failed to provision repo %r → %s: %s. "
                    "Falling back to local BASE_DIR checkout.",
                    repo.alias, dest, exc,
                )
                return str(BASE_DIR)
            path = result.get("path", dest) if isinstance(result, dict) else dest
            if idx == 0:
                primary_path = path
        return primary_path

