    async def _build_plan_summary(
        self, brief: WorkBrief, excerpts: List[str]
    ) -> str:
        """Generate a short action-oriented plan for the Jira ticket comment.

        Uses the plan LLM (``DEV_LOOP_PLAN_LLM`` or falls back to the
        summary LLM). Falls back to a deterministic stub when the LLM
        call fails so the flow never aborts because of this helper.

        Args:
            brief: The validated :class:`WorkBrief` for this run.
            excerpts: Pre-collected log excerpts (may be empty).

        Returns:
            A plan string suitable for posting as a Jira comment body.
        """
        try:
            client = self._get_plan_client()
            prompt = self._compose_plan_prompt(brief, excerpts)
            response = await client.ask(prompt, max_tokens=1200)
            text = (response.response or "").strip()
            if text:
                return text
        except Exception as exc:  # noqa: BLE001 - degraded fallback
            self.logger.warning(
                "Plan summarization via %s failed (%s); "
                "falling back to deterministic stub",
                self._plan_llm, exc,
            )
        return self._deterministic_plan_stub(brief)

    @staticmethod
    def _compose_plan_prompt(
        brief: WorkBrief, excerpts: List[str]
    ) -> str:
        """Build the plan-generation prompt consumed by the plan LLM.

        Args:
            brief: The work brief for this run.
            excerpts: Log excerpts (may be empty).

        Returns:
            A single string combining a system frame and user content.
        """
        return (
            "You are an SRE planning a fix or change for a Jira ticket. "
            "Read the request and produce a SHORT actionable plan "
            "(max 8 bullet points) describing the steps you would take "
            "to resolve it. Reference modules / files when obvious from "
            "the description. Do not quote logs verbatim and do not "
            "restate the summary.\n\n"
            f"Kind: {getattr(brief, 'kind', 'bug')}\n"
            f"Summary: {brief.summary}\n"
            f"Component: {brief.affected_component}\n"
            f"Description: {brief.description or '(none)'}\n"
            "Log excerpts (truncated):\n"
            + ("\n---\n".join(excerpts) if excerpts else "(none)")
        )

    @staticmethod
    def _deterministic_plan_stub(brief: WorkBrief) -> str:
        """Return a deterministic fallback plan when the LLM fails.

        Args:
            brief: The work brief; used to supply context in the stub text.

        Returns:
            A short stub string that is safe to post as a Jira comment.
        """
        return (
            f"Plan unavailable (LLM call failed). Manual triage "
            f"required for kind={getattr(brief, 'kind', 'bug')!r}, "
            f"component {brief.affected_component!r}."
        )

