# MonthlySecuritySummarizer
# ---------------------------------------------------------------------------


class MonthlySecuritySummarizer:
    """Produces a ``MonthlySummary`` from a list of weekly ``WeeklySummary`` objects.

    Diff math operates on the ``persistent_findings`` sets of each weekly
    summary.  The LLM is invoked EXACTLY ONCE per ``build()`` call, solely
    for the ``executive_paragraph``.

    Args:
        llm_client: Any LLM client satisfying the same contract as
            ``WeeklySecuritySummarizer``.
    """

    def __init__(self, llm_client: Any) -> None:
        self._llm = llm_client
        self._logger = logging.getLogger(__name__)

    async def build(
        self,
        weekly_summaries: list[WeeklySummary],
        framework: str,
        provider: str,
    ) -> MonthlySummary:
        """Build a ``MonthlySummary`` from a list of weekly summaries.

        Args:
            weekly_summaries: List of ``WeeklySummary`` objects for the month
                (typically 4, one per week).  May be empty.
            framework: Compliance framework label (e.g., ``"HIPAA"``).
            provider: Cloud provider label (e.g., ``"aws"``).

        Returns:
            ``MonthlySummary`` with deterministic math + LLM executive paragraph.
        """
        now = datetime.now(timezone.utc)
        period_start = min((w.period_start for w in weekly_summaries), default=now)
        period_end = max((w.period_end for w in weekly_summaries), default=now)

        # 1. Deterministic severity totals: sum across weekly summaries.
        totals = SeverityBreakdown(
            critical=sum(w.severity_totals.critical for w in weekly_summaries),
            high=sum(w.severity_totals.high for w in weekly_summaries),
            medium=sum(w.severity_totals.medium for w in weekly_summaries),
            low=sum(w.severity_totals.low for w in weekly_summaries),
            informational=sum(w.severity_totals.informational for w in weekly_summaries),
        )

        # 2. Monthly persistence: findings present in ALL weeks' persistent sets.
        #    On zero or one week, treat all findings as persistent.
        if not weekly_summaries:
            persistent_findings: list[EmbeddedFinding] = []
        elif len(weekly_summaries) == 1:
            persistent_findings = list(weekly_summaries[0].persistent_findings)
        else:
            # Intersection across all weekly persistent_finding_ids.
            weekly_id_sets = [
                {f.finding_id for f in w.persistent_findings} for w in weekly_summaries
            ]
            persistent_ids = weekly_id_sets[0].intersection(*weekly_id_sets[1:])
            # Pick canonical EmbeddedFinding from the latest week that has it.
            canonical: dict[str, EmbeddedFinding] = {}
            for w in weekly_summaries:
                for f in w.persistent_findings:
                    if f.finding_id in persistent_ids:
                        canonical[f.finding_id] = f  # last week wins
            persistent_findings = _sort_findings(list(canonical.values()))

        # Collect source_report_ids: UUIDs from all weeks' source_report_ids.
        source_report_ids: list[UUID] = [
            rid for w in weekly_summaries for rid in w.source_report_ids
        ]

        # 3. Single LLM call — only for the executive paragraph.
        exec_result = await self._call_llm_for_exec(
            framework=framework,
            provider=provider,
            totals=totals,
            persistent=persistent_findings,
            n_weeks=len(weekly_summaries),
        )

        return MonthlySummary(
            framework=framework,
            provider=provider,
            period_start=period_start,
            period_end=period_end,
            severity_totals=totals,
            persistent_findings=persistent_findings,
            executive_paragraph=exec_result.paragraph,
            source_report_ids=source_report_ids,
        )

    async def _call_llm_for_exec(
        self,
        *,
        framework: str,
        provider: str,
        totals: SeverityBreakdown,
        persistent: list[EmbeddedFinding],
        n_weeks: int,
    ) -> _Executive:
        """Compose monthly prompt and call the LLM for the executive paragraph.

        Args:
            framework: Compliance framework label.
            provider: Cloud provider label.
            totals: Aggregated monthly severity counts.
            persistent: Findings that persisted across all weekly summaries.
            n_weeks: Number of weekly summaries consumed.

        Returns:
            ``_Executive`` Pydantic model with the generated paragraph.
        """
        def _fmt(findings: list[EmbeddedFinding]) -> str:
            if not findings:
                return "none"
            items = "; ".join(f"[{f.severity}] {f.title}" for f in findings[:5])
            suffix = f" (+ {len(findings) - 5} more)" if len(findings) > 5 else ""
            return f"{items}{suffix}"

        prompt = (
            f"Write a 3-5 sentence executive monthly security summary.\n\n"
            f"Framework: {framework}\n"
            f"Provider: {provider}\n"
            f"Weeks covered: {n_weeks}\n"
            f"Monthly severity totals — "
            f"CRITICAL: {totals.critical}, HIGH: {totals.high}, "
            f"MEDIUM: {totals.medium}, LOW: {totals.low}\n"
            f"Persistent findings (present all month): {_fmt(persistent)}\n\n"
            f"Return only the monthly executive paragraph."
        )
        self._logger.debug(
            "Calling LLM for monthly executive paragraph (framework=%s, provider=%s)",
            framework,
            provider,
        )
        response = await self._llm.ask(prompt, structured_output=_Executive, stateless=True)
        return response.structured_output
